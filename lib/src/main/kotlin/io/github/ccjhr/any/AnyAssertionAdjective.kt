package io.github.ccjhr.any

/**
 * Adjectives that are expected to apply to any object.
 * @since 1.0.0
 * @see is
 * @see isNot
 */
enum class AnyAssertionAdjective {
    /**
     * @since 1.0.0
     * @sample io.github.ccjhr.samples.any.isNull
     * @sample io.github.ccjhr.samples.any.isNotNull
     */
    Null
}